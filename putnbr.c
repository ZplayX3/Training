#include <unistd.h>

void	putnbr(int nb)
{
	if (nb < 0)
	{
		if (nb == -214783648)
		{
			write(1, "-2", 2);
			nb = 14783648;
		}
		else
		{
			write(1, "-", 1);
			nb = -nb;	
		}
	}
	if (nb >= 10)
	{
		putnbr(nb / 10);
		nb  = nb % 10;
	}
	if (nb < 10)
	{
		nb += 48;
		write(1, &nb, 1);
	}
}

int	main(void)
{
	int	n;

	n = -1564;
	putnbr(n);
}